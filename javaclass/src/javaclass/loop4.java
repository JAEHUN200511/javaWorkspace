package javaclass;

public class loop4 {
	
// # continue문
// 반복문 안에서 continue문을 만나면 이후의 문장은 수행하지 않고 for문의 처음으로 돌아가 증감식을 수행
	
	
public static void main(String [] argu)

{
	
int total = 0;


for ( int num = 1; num <= 10; num++)
	
{     

      if ( num % 2 == 0) // num 값이 짝수인 경우
      continue;          // 이후 수행을 생략하고 num++ 수행
      total += num;      // num 값이 홀수인 경우에만 수행
      


	
	
	
	
}
	
	
	
	
	
}
	
	
	
	
	
	
	
	
	

}
