package project;

public class spec12 {
	
// 산술 연산자는 +[덧셈], -[뺄셈], *[곱셈], /[나눗셈], 나머지[%] 총 5가지가 있다
/*
 *  /[나눗셈]은 나누어 몫을 구한다
 *  %[나머지]는 나누어 나머지를 구한다
 *  
 *  산술 연산자의 우선순위는 일반 수학과 같다
 *  
 *  
 */
	
	public static void main(String[] args)
	{
		
	int mathscore = 90;
	int engscore = 70;
	int korscore = 100;
	
	int totalScore = mathscore + engscore + korscore; //총점 구하기
	System.out.println(totalScore);
	
	int avgscore = totalScore / 3; //평균 구하기
	System.out.println(avgscore);
	
	
	
	
		
		
		
		
		
		
		
	}
	
	
	
	
}
